/*
1. Contador de 1 a 10
Use um for para mostrar no console os 
números de 1 até 10, um por linha.
*/

for (let i = 1; i <= 10; i++) {
    console.log(i);
}

let cont = 1;
while (cont <= 10) {
    console.log(cont);
    cont++;
}

let cont2 = 1;
do{
    console.log(cont2);
    cont2++;
}while (cont2 <= 10);